// funcao somar 2 params - console.log(soma)
// funcao subtrair 2 params - console.log(subtracao)

function somar(a, b) {
  console.log(a + b);
}

function subtracao(a, b) {
  console.log(a - b);
}

somar(3, 4);
somar(31, 98);

subtracao(11, 7);
subtracao(19, 52);

const somar2 = (a, b) => console.log(a + b);

somar2(3, 5);
somar2(33, 52);