// Duas expressões que vão resultar em TRUE (Aritméticos/Relacionais/Lógicos)
let verdadeira1 = 2 + 2 >= 4 && 2 - 2 <= 0;
let verdadeira2 = 5 + 5 <= 10 || 5 + 5 < 15;

console.log(verdadeira1);
console.log(verdadeira2)

// Duas expressões que vão resultar em FALSE (Aritméticos/Relacionais/Lógicos)
let falso1 = 2 + 2 >= 4 && 2 + 2 > 5;
let falso2 = 5 + 6 <= 10 || 5 + 6 > 15;

console.log(falso1);
console.log(falso2);